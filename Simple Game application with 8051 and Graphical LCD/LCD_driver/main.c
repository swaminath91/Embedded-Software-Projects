void Delay (unsigned int time)
{
    unsigned int i,j;
    for (i =0; i<time; i++)
        for(j=0; j<1275; j++);
}

void lcdbusywait(void)							// Polls the LCD busy flag.
{
    LCD_RS = 0;									// Register Select high for instruction operation
    LCD_RW = 1;                               	// R/W low for read
    eptr = LCD_ADDRESS;							// Setup global pointer to address F000
    while ((*eptr & BUSY_MASK) !=0);			// Wait till Busy flag is 1
}


void lcd_cmd(char cmd)				            // Write a command to the LCD.
{
    lcdbusywait();                              // Check whether LCD is ready or busy?
    LCD_RS = 0;                            		// Register Select low for instruction operation
    LCD_RW = 0;                             	// R/W low for write
    eptr = LCD_ADDRESS;							// Setup global pointer to address F000
    *eptr = cmd;	                            // Write the command
}


void lcdputch(char cc)					    	// Write a character to the LCD.
{
    lcdbusywait();                              // Check whether LCD is ready or busy?
    LCD_RS = 1;                              	// Register Select high for data operation
    LCD_RW = 0;                             	// R/W low for write
    eptr = LCD_ADDRESS;							// Setup global pointer to address F000
    *eptr = cc;	                                // Write the value
}



